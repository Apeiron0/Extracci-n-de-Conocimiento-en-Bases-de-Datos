#¿Cuántos hombres harán en 19 días un trabajo que 209 hombres pueden hacer en 10 días?

hombres= 209
dias= 10
nuevos_dias = 19


trabajo_total = hombres* dias

hombres_necesarios = trabajo_total // nuevos_dias

print(f"Se necesitarán exactamente: {hombres_necesarios} hombres para terminar el trabajo en {nuevos_dias} días.")